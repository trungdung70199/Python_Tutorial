# Built in Math  Functions
x = min(1, 2, 3, 4)
y = max(1, 2, 4, 3)

print(x)
print(y)

# The abs() function returns the absolute(positive) value 
x = abs(-7.35)
print(x)

# Return the value of 4 to the power of 3
x = pow(4, 3)
print(x)

# The Math Module
# Use import math
import math 
x = math.sqrt(64)
print(x)

import math
x = math.ceil(1.4)
y = math.floor(1.4)
